package org.javaai.stablediffusion.api;

public class StableDiffusionException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1665673499512159486L;

	public StableDiffusionException() {
		super();
	}

	public StableDiffusionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public StableDiffusionException(String message, Throwable cause) {
		super(message, cause);
	}

	public StableDiffusionException(String message) {
		super(message);
	}

	public StableDiffusionException(Throwable cause) {
		super(cause);
	}

	
	
	
	
	
	
	
}
